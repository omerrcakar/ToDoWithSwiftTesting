import Foundation

class MathFunctions {
    
    func addIntregers(_ a: Int, _ b: Int) -> Int {
        return a + b
    }
    
    func multiplyIntregers(_ a: Int, _ b: Int) -> Int {
        return a * b
    }
    
    func minusIntregers(_ a: Int, _ b: Int) -> Int {
        return a - b
    }
    
    func divideIntregers(_ a: Int, _ b: Int) -> Int {
        return a / b
    }
    
}
